package com.itwillbs.domain;


public class StockDTO {
	private String item_num;
	private String item_name;
	private String item_type;
	private int stk_qnt;
	private String whs_num;
	private String whs_name;
	
	public String getItem_num() {
		return item_num;
	}
	public void setItem_num(String item_num) {
		this.item_num = item_num;
	}
	public String getItem_name() {
		return item_name;
	}
	public void setItem_name(String item_name) {
		this.item_name = item_name;
	}
	public String getItem_type() {
		return item_type;
	}
	public void setItem_type(String item_type) {
		this.item_type = item_type;
	}
	public int getStk_qnt() {
		return stk_qnt;
	}
	public void setStk_qnt(int stk_qnt) {
		this.stk_qnt = stk_qnt;
	}
	public String getWhs_num() {
		return whs_num;
	}
	public void setWhs_num(String whs_num) {
		this.whs_num = whs_num;
	}
	public String getWhs_name() {
		return whs_name;
	}
	public void setWhs_name(String whs_name) {
		this.whs_name = whs_name;
	}
	
	
	
}
